public class Ahorro {
    private int id;
    private String cantidad;

    public Ahorro(){
        this.id = id;
        this.cantidad = cantidad;
    }

    public void getId(int id){
        this.id = id;
    }

    public void getCantidad(String cantidad){
        this.cantidad = cantidad;
    }

    public void setId(int id){
        this.id = id;
    }

    public void setCantidad(String cantidad){
        this.cantidad = cantidad;
    }
}
